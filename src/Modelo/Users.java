package Modelo;

/**
 *
 * @author dh057
 */
public class Users extends Persons{
    private String user;
    private String password;
    private String mail;
    private int phone;

    public Users(String user, String password, String mail, int phone, String cedula, String name) {
        super(cedula, name);
        this.user = user;
        this.password = password;
        this.mail = mail;
        this.phone = phone;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }
    
}
