    private ImageIcon cargarIcono(String path, int w, int h) {
        URL url = getClass().getResource(path);
        if (url == null) {
            return null;
        }

        Image img = new ImageIcon(url).getImage().getScaledInstance(w, h, Image.SCALE_SMOOTH);
        return new ImageIcon(img);
    }

